package com.frameworkset.common.tag.export.properties;


/**
 * 封装导出配置实体对象
 * @author biaoping.yin
 * created on 2005-5-14 
 * version 1.0
 */
public class ExportConfig {
	/**描述信息*/
	private String description;
	
	/**版本信息*/
	private String version;
	
	/**名称*/
	private String name;
	
	/**类型*/
	private String type;
	
	/**类型*/
	private String exportClass;	
	
	

	
	public ExportConfig()
	{
		
	}
	
	public String toString()
	{
		StringBuffer ret = new StringBuffer()
								.append("[description:")
								.append(getDescription())
								.append(",name:")
								.append(this.getName())
								.append(",type:")
								.append(getType())
								.append(",exportClass:")
								.append(getExportClass())
								.append("]");
		return ret.toString();
	}

	/**
	 * @return Returns the description.
	 */
	public String getDescription() {
		return description;
	}
	/**
	 * @param description The description to set.
	 */
	public void setDescription(String description) {
		this.description = description;
	}
	/**
	 * @return Returns the exportClass.
	 */
	public String getExportClass() {
		return exportClass;
	}
	/**
	 * @param exportClass The exportClass to set.
	 */
	public void setExportClass(String exportClass) {
		this.exportClass = exportClass;
	}
	/**
	 * @return Returns the name.
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name The name to set.
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return Returns the type.
	 */
	public String getType() {
		return type;
	}
	/**
	 * @param type The type to set.
	 */
	public void setType(String type) {
		this.type = type;
	}
	/**
	 * @return Returns the version.
	 */
	public String getVersion() {
		return version;
	}
	/**
	 * @param version The version to set.
	 */
	public void setVersion(String version) {
		this.version = version;
	}
}
